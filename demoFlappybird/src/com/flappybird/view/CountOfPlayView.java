     private List<String> countOfPlay(){
        GameService_Service service = new GameService_Service();
        GameService port = service.getGameServicePort();
        return port.countOfPlay();
    }
     
     private void showData(){
        countModel.setRowCount(0);
        int count = 1;
        List<String> list = countOfPlay();
        
        for (String str : list) {
            String [] data = str.split(" ");
            String username = data[0];
            String countOfPlay = data[1];
            
            countModel.addRow(new Object[] {
                count++, 
                username,
                countOfPlay               
            });
        }
     }

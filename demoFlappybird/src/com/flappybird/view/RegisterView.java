    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        boolean result;
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String name = txtName.getText();
        String phone = txtPhone.getText();
        User user = new User();
        user.setUsername(username);
        user.setPassword(password);
        user.setName(name);
        user.setPhone(phone);
        if(username.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Username is required!");
            return;
        }
        if(password.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Password is required!");
            return;
        }
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Name is required!");
            return;
        }
        
        result = saveRegister(user);
        if(result == false){
            JOptionPane.showMessageDialog(rootPane, "Username has already exists");
            return;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Register Success");
            return;
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

